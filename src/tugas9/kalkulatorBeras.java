public class kalkulatorBeras {
    
   void kali(double x, double y) {
        
        System.out.print(x+"X"+y+"="+(x*y));
        
    }
    
    void kurang(double x, double y) {
        System.out.print(x+"x"+y+"="+(x-y));
    }
    
    void tambah(double x, double y) {
        
        System.out.print(x+"+"+y+"="+(x+y));
      
    }
    
    void bagi(double x, double y) {
        
        System.out.print(x+":"+y+"="+(x/y));
    }
    
}
